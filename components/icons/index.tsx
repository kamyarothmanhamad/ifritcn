// Cluster icons — used in service cards
export function ClusterIcon({ cluster, size = 24 }: { cluster: string; size?: number }) {
  const props = { size, className: "text-crimson" };
  switch (cluster) {
    case "find-buy":          return <IconSearch {...props} />;
    case "protect-ship":     return <IconShield {...props} />;
    case "build-grow":       return <IconSprout {...props} />;
    case "represent-support":return <IconHandshake {...props} />;
    default:                  return <IconSearch {...props} />;
  }
}

// Service icons — one per slug
